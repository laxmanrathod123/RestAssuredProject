package POJO;

public class GetCourses {
	private String instructor;
	private String url;
	private String services;
	private courses courses;
	private String linkdedIn;
	private String exepertise;
	
	
	public String getInstructor() {
		return instructor;
	}
	public void setInstructor(String instructor) {
		this.instructor = instructor;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getServices() {
		return services;
	}
	public void setServices(String services) {
		this.services = services;
	}
	public POJO.courses getCourses() {
		return courses;
	}
	public void setCourses(POJO.courses courses) {
		this.courses = courses;
	}
	public String getLinkdedIn() {
		return linkdedIn;
	}
	public void setLinkdedIn(String linkdedIn) {
		this.linkdedIn = linkdedIn;
	}
	public String getExepertise() {
		return exepertise;
	}
	public void setExepertise(String exepertise) {
		this.exepertise = exepertise;
	}
	
	
	

}
